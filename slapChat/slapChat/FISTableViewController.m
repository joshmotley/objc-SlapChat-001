#import "FISTableViewController.h"
#import "FISDataStore.h"
#import "Message+DateFormatter.h"

@interface FISTableViewController ()
@property (strong, nonatomic) NSArray *messages;

@end

@implementation FISTableViewController


- (void)viewDidLoad
{
    [super viewDidLoad];
    
    FISDataStore *dataStore = [FISDataStore sharedDataStore];
    self.messages = [dataStore fetchMessages];
}

- (void)didReceiveMemoryWarning
{
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

- (IBAction)sortButtonTapped:(id)sender {
    
    NSSortDescriptor *descriptor = [[NSSortDescriptor alloc] initWithKey:@"createdAt" ascending:NO];
    self.messages = [self.messages sortedArrayUsingDescriptors:@[descriptor]];
    [self.tableView reloadData];
}
#pragma mark - Table view data source


- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    return self.messages.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"basicCell" forIndexPath:indexPath];
    
    Message *currentMessage = self.messages[indexPath.row];
    
    cell.textLabel.text = currentMessage.content;
    cell.detailTextLabel.text = [currentMessage formattedDate];
    
    return cell;
}

